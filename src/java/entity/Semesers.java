package entity;

/**
 *
 * @author haich
 */
public class Semesers {
    private String id;
    private String name ;
    private Subject subject;

    public Semesers() {
    }

    public Semesers(String id, String name, Subject subject) {
        this.id = id;
        this.name = name;
        this.subject = subject;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Subject getSubject() {
        return subject;
    }

    public void setSubject(Subject subject) {
        this.subject = subject;
    }
    
    
            
}
